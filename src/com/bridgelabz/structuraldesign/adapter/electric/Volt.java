package com.bridgelabz.structuraldesign.adapter.electric;

/**
 * getter setter methods to get Volts And set Volts
 *
 */
public class Volt {
	private int volts;

	public Volt(int v) {
		this.volts = v;
	}

	public int getVolts() {
		return volts;
	}

	public void setVolts(int volts) {
		this.volts = volts;
	}
}
